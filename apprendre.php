            <h2>1. Historique et statut de la zakat</h2>
            <p>

Encore appelée sadaqa ou aumône légale, la zakat constitue le troisième pilier de l’islam après la shahada et la pri&eacute;re. Citée 28 fois dans le coran, la zakat a été instituée durant l’an 02 de l’hégire. Son caractère obligatoire trouve son fondement dans le Coran, dans la sunna et dans le consensus des oulémas. Allah dit dans le Coran : « prélève une sadaqa sur leurs biens pour les purifier et les bénir. Prie pour eux, afin que leurs âmes retrouvent leur quiétude. Allah est Audient et Omniscient. » Muslim rapporte : A Mouâdh (rta) lorsque le Prophète (saws) l’avait envoyé vers le Yemen. Il lui avait interpelé en ce sens: « Tu vas aller auprès d’un peuple faisant partie des Gens du Livre. Invite-les à attester qu’il n’y a de Dieu qu’Allah et que je suis le Messager d’ Allah. S’ils t’obéissent sur ce point, alors fais leur savoir qu’Allah a rendu obligatoire sur eux cinq prières quotidiennement. S’ils t’obéissent sur ce point (aussi), alors informe-les qu’Allah leur a imposé une aumône qui sera prise de leurs riches pour être redistribuée à leurs pauvres (…) »


Sa collecte et sa distribution était, autrefois, gèrer par les dirigeants politiques musulmans. Mais à la disparition du pouvoir d'Emir des croyants et l'emmiettement des empires musulmanes, l'apprentissage et l'acquittement de la zakat est devenu une nécessité pour tout musulman que Dieu à confier une richesse.

La zakat dans son sens étymologique signifie purification. Son but est donc de purifié l'âme de celui qui s'en acquitte de l'avarice et de la cupidité; mais aussi une incitation à la solidarité avec les personnes nécessiteuses.
