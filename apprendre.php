            <h2>4. Les bénéficiaires</h2>
            <p>
            Dieu dit dans le Noble Coran : <em><< Les aumônes (légales) ne doivent revenir
qu’aux besogneux et aux indigents, à la rétribution de ceux qui les perçoivent,
au ralliement des cœurs, à (affranchir des nuques) esclaves, à libérer des
débiteurs, au combat sur le chemin de Dieu et à secourir le fils du chemin :
autant d’obligations de par Dieu, Dieu est Connaissant et Sage. >></em> sourate
Tawba, verset 60		
<ul>
	<li>Les besogneux et les indigents</li>
Les besogneux (faqîr) qui ne possèdent qu’une partie de l’indispensable, et les
indigents (miskîn) qui ne peuvent se procurer l’indispensable1), ont droit au
partage des aumônes légales collectées.On croira à toute déclaration de pauvreté ou d’indigence, à moins que quelque
indication douteuse ne paraisse. Pour avoir droit à une part des aumônes légales,
les besogneux et les indigents doivent :
	<ul>
		<li> être musulmans ;</li>
		<li> manquer d’une partie même médiocre de l’indispensable pour
			vivre une année, ou de ce qui suffit aux dépenses communes d’entretien ;</li>
		<li> n’avoir ni métier, ni genre d’industrie qui puisse fournir aux besoins et à
			la subsistance de la personne et de sa famille</li>
		<li> ne point appartenir, en ligne de filiation mâle directe, à la famille de
			Hâshim.</li>
	</ul>

	<li>Ceux dont on veut gagner le coeur</li>
On accorde une part des aumônes légales aux non musulmans qui peuvent être
de quelque secours et qui penchent à se convertir à l’Islâm. On leur concède une
part des aumônes légales pour les décider à professer la religion islamique.
Toutefois, pour accorder cette part, il faut en examiner et en reconnaître
l’opportunité ; autrement dit, on ne donne à ces non musulmans que
lorsque l’on peut avoir besoin d’eux, ou lorsqu’on est sûr de leur désir sincère de
se convertir à l’Islâm, et que leur admission au partage peut les y décider
	<li>Les débiteurs</li>
On accorde une part des aumônes légales à une personne pour qu’elle s’acquitte
de ses dettes, à condition d’être musulmane et d’origine non Hâshimite. On donnera
une part des aumônes légales à ce débiteur en question, lors même qu’il est
mort. En outre, il faut :
	<ul>
		<li>que la dette contractée n’ait pas un caractère religieux (telle qu’une dette
			d’une aumône légale qui n’a pas été acquittée, ou une dette d’une
			expiation d’une faute ou d’un crime);</li>
		<li>qu’elle ne soit pas contractée par suite d’une mauvaise conduite (par la
			débauche, l’usage de boissons alcoolisées…) ;</li>
		<li> qu’elle soit de nature à entraîner l’incarcération en cas de non
			remboursement ;</li>
		<li>qu’elle ne soit pas contractée dans l’espoir de prendre part aux aumônes
			légales.</li>
		<li>que le débiteur ait livré à ses créanciers ce qu’il a de numéraire entre les
			mains et le surplus de ce qui lui est indispensable.</li>
	</ul>	

	<li>Les combattants sur le chemin de Dieu</li>
Une part des aumônes légales est consacrée :
	<ul>
		<li>aux hommes musulmans capables de porter les armes qui combattent sur
le chemin de Dieu ;</li>
		<li>à l’acquisition ou à la préparation des armes et des engins de guerre dont
les soldats ont besoin</li>
Cette part des aumônes légales est consacrée aux combattants, lors même que
les combattants sont riches. On admet aussi au partage des aumônes légales, les
espions que l’on envoie observer les mouvements de l’ennemi.
Par contre, rien ne doit être employé des aumônes légales pour :
		<li>les constructions et l’entretien des fortifications ;</li>
		<li>les travaux de guerre défensive contre les ennemis ;</li>
		<li>les constructions de vaisseaux de guerre ;</li>
		<li>les constructions de mosquées, de ponts ;</li>
		<li>l’ensevelissement des morts.</li>
	</ul>
	<li>Les fils du chemin</li>
		Il s’agit de l’étranger musulman, non Hâshimite qui a besoin de secours pour
		regagner son pays. On lui donnera sur les aumônes légales ce qui est nécessaire
		pour ses frais de route, à condition :
	<ul>
		<li>que son voyage n’ait point été entrepris dans un but illicite ou criminel</li>
		<li>qu’il ne trouve personne qui veuille lui prêter ce dont il a besoin ;</li>
		<li>qu’il ne reste pas dans le pays où il a reçu des secours pour continuer son
			voyage.</li>
	</ul>
	L’étranger voyageur qui déclare être dans le besoin sera cru sur parole.
<em>Mode de partage des aumônes légales
On accorde aux besogneux et aux indigents une part des aumônes légales
double de celle des sept autres catégories de bénéficiaires prises ensemble. Le
reste est partagé en lots égaux pour chaque catégorie qui est présente. Mais
dans la distribution des aumônes légales, les employés, comme il a été dit,
doivent être payés en premier lieu</em>
</ul></p>
