            <h2>3. Cat&eacute;gories de bien soumises et principe de pr&eacute;levement</h2>
            <p>
            L’aumône légale n’est exigible que pour cinq catégories de biens :
            les bestiaux, comprenant les chameaux, les bovins, les ovins et les caprins
            les produits du sol
            l’or et l’argent
            les métaux et les trésors trouvés
            le capital commercial (‘urûd at-tijâra).
</p>
<ol> 
<li>Les produits agricoles</li>

<p>Cette catégorie renferment l’ensemble des céréales : du riz au maïs, en passant par le mil, l’orge, le blé dur et le blé tendre, le sorgho, le seigle, l’avoine, le sarrasin ainsi que les céréales bio...
Il figure aussi dans cette même catégorie les légumineuses à grains et les oléagineux comme les pois chiches, les haricots, les lentilles, les lupins, l’arachide, les olives et les noix etc.
La dernière classe de cet ensemble est les fruits : les dattes, les raisins…
La quantité minimale imposant l’aumône légale appelée nissab est évaluée à 612Kg.

Si les récoltes sont produites au moyen d’une irrigation artificielle c'est-à-dire par effort d’arrosage de l’homme ou en achetant l’eau, le taux de la zakat est le vingtième de la production.
Si les terres ont été arrosées par l’eau du ciel, par les sources ou par des canaux de dérivation alos le taux de la zakat est : le dixième de la récolte
Si les deux moyens d’arrosage sont combinés, le calcul se fait en tenant compte de la proportion en durée de chaque moyen d’arrosage Par exemple pour une saison de 5 mois si durant 3 mois le cultivateur a irriguée, et pendant 2 mois le champ est arrosée par l’eau des pluies alors : Taux de la zakat= 1/20 x 3/5 +1/10 x 2/5 = 7/100 de la récoltes.
</p>
<li> Les bestiaux

<p>Cette catégories renferment les chameaux et dromadaires, les bovins (vache, veau, génisse…) les ovins (moutons, mouflons…) et les caprins (chèvres…) qui sont domestiqués.
Dans le calcul de la zakat sur le bétail, il faut bien respecter l’âge requis pour la bête à donner.

En plus des conditions posées ci-dessus, il faut, pour être assujetti à l’aumône légale sur les bestiaux, que ces animaux aient été possédés depuis une année lunaire complète. Peu importe qu’il s’agisse d’animaux qui paissent, animaux de travail ou produits vivants de ces troupeaux.
</p>
<ol type="a">
<li>Les chameaux</li>
<p>
Le nissab pour les chameaux est 05 bêtes. En dessous de cela, la zakat n’est pas obligatoire.
Le taux de l’aumône légale sur les chameaux est fixé à:
une brebis à partir de 5 chameaux jusqu’à 09;
de 10 à 14, deux brebis ;
de 15 à 19, trois brebis ;
de 20 à 24, quatre brebis ;
de 25 à 35, une chamelle âgée d’un an accompli ;de 36 à 45, une chamelle de deux ans accomplis ;
de 46 à 60, une chamelle de trois ans accomplis ;
de 61 à 75, une chamelle de quatre ans accomplis ;
de 76 à 90, deux chamelles de deux ans accomplis ;
de 91 à 120, deux chamelles de trois ans ;
de 121 à 129, deux chamelles de trois ans accomplis, ou bien trois chamelles de deux ans.
Au delà du chiffre de 129, on comptera une chamelle de deux ans accomplis pour chaque quarantaine de têtes, et une de trois ans accomplis pour chaque cinquantaine.
La brebis dont on parle ci-dessus est soit un mouton de 2 ans soit une chèvre selon l’espèce la plus présente dans la région. S’ils codominent, alors le choix est porté sur le mouton.
</p>
<li> Les bovins</li>

<p>Nous avons préféré utiliser le terme bovin pour respecter les critères de nomenclature.

Veau : bovin mâle ou femelle de la naissance jusqu’à 6 mois.
Génisse : bovin femelle âgé de plus de 6 mois qui n’a pas vêlé. La génisse devient une vache dès son premier veau.
Jeune bovin ou taurillon : bovin mâle âgé de 6 à 24 mois.
Vache : femelle ayant vêlé.
Bœuf : mâle adulte âgé de plus de 24 mois castré.
Taureau : mâle adulte âgé de plus de 24 mois non castré.

Le nissab pour les bovins est de 30 bêtes.
La zakat sur les bovins est fixée à une bête de deux ans accomplis pour chaque
nombre de 30 bêtes et d’une bête de trois ans accomplis pour chaque nombre de
40 têtes. Ce taux reste le même jusqu’à la limite de 59.
De 60 à 69, il est de deux bêtes de deux ans.
A partir de 70 bêtes, chaque nombre 30 et chaque nombre 40 qui entre dans la
composition de la totalité du troupeau donnera, le premier (c’est-à-dire, lenombre 30),
un prélèvement d’un e bête de deux ans, et le second
(c’est-à-dire le nombre 40), un prélèvement d’une bête de trois ans.
</p>

<li> Les ovins et les caprins</li>
<p>
Cet ensemble renferme les moutons, les brebis, les chèvres, les
boucs… le calcul la zakat se fait de la même manière aussi bien chez
les moutons que chez les chèvres. Les moutons et les chèvres se
comptent ensemble dans un même troupeau.
Le nissab est de 40 bêtes.
La zakat est fixé à une bête d’un an accompli pour chaque 40 têtes, jusqu’à 120.

De 121 têtes jusqu’à 200, l’aumône légale est de deux bêtes d’un an accompli.
De 201 têtes jusqu’à 399, l’aumône légale est de trois bêtes.
De 400 têtes, l’aumône légale est de quatre bête ;
A partir 500 têtes, cinq bêtes, et ainsi de suite pour chaque 100 en surplus, une bête.

Le propriétaire livrera des animaux de moyenne taille et de moyenne qualité,
quelle que soit l’espèce d’animaux, chameaux ou chamelles, veaux ou vaches ou
buffles, ou brebis ou chèvres ou boucs. Mais il ne livrera jamais d’animaux audessous de
l’âge déterminé par la Loi révélée.
</p>
</ol>
</li>

<li> L’or, l’argent et les monnaies fiduciaires
<p>
L’or est aujourd’hui la monnaie de référence dans le calcul de la zakat pour la monnaie fiduciaire en raison de sa stabilité. Quand à l’argent (métal précieux), son cours a subit de nombreuses variations depuis l’époque du prophète saws.

Pour que le fidèle soit assujetti à l’aumône légale sur l’or et l’argent, il faut qu’il
possède la somme depuis une année lunaire entière. L’année de possession ne
commence que du moment de l’entrée en jouissance. Dans la Sunna : « Pas
d’aumône légale sur une somme acquise avant qu’une année entière se soit
écoulée. » In at-Tirmidhî. Si, après avoir possédé la somme minimum imposable
(nisâb) en début d’année, le fidèle n’a plus cette somme durant un temps et la
retrouve en fin d’année, il acquittera dessus l’aumône légale
</p>
<ol type="a">
<li>L’or</li>
<p>
le nissab en or est de 20dinars d’or de 22 carats. Cette somme est l’équivalent de 85grammes
d’or. Le montant dû est le quarantième de la fortune en or.
</p>
<li>L’argent</li>
<p>
Le nissab en argent est de200 dirhams d’argent équivalent à 595g d’argent pûr.
La quantité à prélever est le même : le quarantième.
</p>
<li>Les monnaies d’échanges</li>
<p>
Le nissab se calcul en faisant la conversion de 85g d’or en €, francs CFA ou en tout autre devise.
Par exemple, à la date du 08 février 2018 le nissab en € est : 2525,35 € et en CFA : 1.656.521,5 francs
Le taux de la zakat est encore le quarantième.
</p>
</ol>
</li>
<li>Le capital commercial (les marchandises..)</li>
<p>
Sont aussi soumis à l’aumône légale, les marchandises (tout produit commercial) ainsi que les biens à louer.
Pour que le fidèle soit assujetti à l’aumône légale sur le capital commercial, il faut :

que les objets ne soient pas soumis à l’aumône légale en nature sur leur prix ou leurs valeurs (par exemple, comme le subissent les troupeaux et les produits du sol) ;.
qu’ils soient acquis en toute propriété par voie d’échange ou de transaction (non par voie d’héritage, de donation, de cadeaux, de don nuptial, de dommages-intérêts…)
qu’il ait l’intention de les mettre en spéculation de commerce ou de les faire fructifier par voie de louage ou de les louer et de les vendre ensuite (lorsqu’il y aura un bénéfice à en retirer) ou de les vendre après en avoir fait usage ;
que ces objets soient vendus et soient payés en espèces ;
qu’une année lunaire se soit écoulée.

La manière d’évaluer l’aumône légale sur le capital commercial est la suivante :

dans le cas d’un commerce actif, c'est-à-dire les produits sont successivement renouvelés comme dans les boutiques, les grossistes, le commerçant fait l’inventaire de ses marchandises plus ce qu’il possède en main en somme d’argent.
Dans le cas de la location, c’est sur la somme des revenus mensuels de la location que le prélèvement de la zakat s’exerce et non sur le produit.
</p>
<li>Les minerais et les trésors enfouis</li>
<p>
Dans le rite malikite, le métal correspond à tout corps se trouvant au sein de la
terre, comme les veines d’or et d’argent, le cuivre, le plomb, le soufre, le sel, le
pétrole, ou dans la mer comme les perles, le corail, etc. Les trésors enfouis
correspondent à l’or et à l’argent cachés sous terre par les hommes, comme
faisaient les Arabes païens. La différence entre métal et trésor se rapporte au fait
que le métal est d’origine terrestre — donc créé par Dieu —, alors que le trésor
est une épargne mise sous terre par les soins de son propriétaire.
L’aumône sur les trésors trouvés sans peine est le cinquième : ceci quelque soit
la quantité. S’il a peiné à les obtenir alors l’impôt s’évalue au quarantième si le
nissab est atteint. Cette zakat doit être distribuée aux pauvres au jour ou la
richesse est récoltée.
Concernant les métaux, excepté l’or et l’argent, la zakat sur les métaux n’est pas
prescrite. La quantité redevable sur l’or et l’argent est le quart du dixième.
<p>
<li> La zakat d’el-Fitr</li>
<p>
La zakat d’El-Fitr est l’un des pratiques prescrit par le Prophète saws à tout musulman au terme du mois de ramadan, précisément, à la veille de l’aïd El-Fitr. La quantité de 2,176kilogrammes (sensiblement 2kg) est à donner exclusivement aux musulmans pauvres. De préférence, la nature de l’aumône doit être l’aliment le plus consommé dans le pays que ce soit le riz, les dattes ou les lentilles… Les savants ont approuvé la possibilité de s’acquitter de l’aumône de la rupture du jeune par la somme équivalente d’argent. Le père de famille s’acquitte de se devoir ainsi que pour les musulmans qui sont à sa charge (leur nutrition). Ce sont :

ses enfants mâles, jusqu’à ce qu’ils entrent en puberté et puissent gagner leur vie ;
ses filles, jusqu’à ce qu’elles soient mariées ou demandées en mariage ;
son père et sa mère, s’ils sont pauvres ;
sa femme ou ses femmes
la femme ou les femmes de son père
les domestiques

            </p>
